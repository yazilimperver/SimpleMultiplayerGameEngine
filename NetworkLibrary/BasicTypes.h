#ifndef BASICTYPES_H__
#define BASICTYPES_H__

#include <cstddef>

typedef std::byte              uByte;
typedef char		           uChar;
typedef unsigned char          uUChar;
typedef const char*	           uConstCharArray;
typedef short                  uInt16;
typedef unsigned short         uUInt16;
typedef int                    uInt32;
typedef unsigned int           uUInt32;
typedef long long int          uInt64;
typedef unsigned long long int uUInt64;
typedef float                  uDecimal32;
typedef double                 uDecimal64;
typedef bool		           uBool;

#endif // BASICTYPES_H__

